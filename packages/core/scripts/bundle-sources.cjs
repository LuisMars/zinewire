#!/usr/bin/env node
/**
 * Reads zinewire Python source files and CSS themes, emits them as a JS module.
 * Run: node packages/core/scripts/bundle-sources.js
 */
const fs = require("fs");
const path = require("path");

const ZINEWIRE_SRC = path.resolve(
  __dirname,
  "../../../src/zinewire"
);
const OUTPUT = path.resolve(__dirname, "../src/sources.js");

const pythonFiles = [
  "__init__.py",
  "config.py",
  "converter.py",
  "directives.py",
  "imposition.py",
  "manual.py",
  "paginator.py",
  "tables.py",
  "templates.py",
];

const pythonSources = {};
for (const file of pythonFiles) {
  const filePath = path.join(ZINEWIRE_SRC, file);
  pythonSources[file] = fs.readFileSync(filePath, "utf-8");
}

// Also include themes/__init__.py
const themesInit = path.join(ZINEWIRE_SRC, "themes", "__init__.py");
if (fs.existsSync(themesInit)) {
  pythonSources["themes/__init__.py"] = fs.readFileSync(themesInit, "utf-8");
}

const themesDir = path.join(ZINEWIRE_SRC, "themes");
const themeCss = {};
for (const file of fs.readdirSync(themesDir).filter((f) => f.endsWith(".css"))) {
  themeCss[file] = fs.readFileSync(path.join(themesDir, file), "utf-8");
}

// Read shared config form HTML + CSS
const configFormHtml = fs.readFileSync(path.join(ZINEWIRE_SRC, "config-form.html"), "utf-8");
const configFormCss = fs.readFileSync(path.join(ZINEWIRE_SRC, "config-form.css"), "utf-8");

const output = `// Auto-generated by bundle-sources.js — do not edit
export const pythonSources = ${JSON.stringify(pythonSources, null, 2)};

export const themeCss = ${JSON.stringify(themeCss, null, 2)};

export const configFormHtml = ${JSON.stringify(configFormHtml)};

export const configFormCss = ${JSON.stringify(configFormCss)};
`;

fs.writeFileSync(OUTPUT, output, "utf-8");

// Also emit a JSON file for the VSCode extension (safe to parse, no regex needed)
const JSON_OUTPUT = path.resolve(__dirname, "../src/sources.json");
fs.writeFileSync(
  JSON_OUTPUT,
  JSON.stringify({ pythonSources, themeCss }, null, 2),
  "utf-8"
);

console.log(
  `Bundled ${Object.keys(pythonSources).length} Python files + ${Object.keys(themeCss).length} CSS themes → ${OUTPUT} + ${JSON_OUTPUT}`
);
